  private DefaultTableModel preencherClientes(List<Clientes> lista) {
        String[] colunas = {"ID", "Nome", "CPF", "Telefone", "Endere√ßo"};

        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);

        for (int i = 0; i < lista.size(); i++) {
            Clientes c = lista.get(i);

            String[] linha = {
                Integer.toString(c.getId()),
                c.getNome(),
                c.getCpf(),
                c.getTelefone(),
                c.getEndereco()

            };
            tabela.addRow(linha);
        }

        return tabela;
    }
